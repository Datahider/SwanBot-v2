Вы собираетесь сгенерировать <?=$codes_count;?> кодов активации.

Параметры кодов:
- Количество дней продления: <b><?=$codes_days;?></b>
- Срок действия в днях: <b><?=$codes_valid;?></b>
- Количество активаций: <b><?=$codes_activations;?></b>

Для генерации кодов с указанными параметрами нажмите кнопку.